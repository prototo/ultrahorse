    public int getTile(int x, int y) {
        return tiles[y][x];
    }

    public void draw(Graphics g) {
        for (int x = 0; x < dim; x++) {
            for (int y = 0; y < dim; y++) {
                if (tiles[y][x] != 0) {
                    g.setColor(Color.cyan);
                    g.fillRect(x * tilew, y * tileh, tilew, tileh);
                }
            }
        }
    }
